/*
 * Small function for returns the minimum value in the array
 *
 * @param {Object} ...arr - Tables to be combined
 * @example min([3, 10, 40, 1101010]) 
 * @return {Number} 3
 */

const min = arr => Math.min(...arr)


/*
 * Small function for returns the maximum value in the array
 *
 * @param {Object} ...arr - Tables to be combined
 * @example min([3, 10, 40, 1101010])
 * @return {Number} 1101010
 */

const max = arr => Math.max(...arr)
